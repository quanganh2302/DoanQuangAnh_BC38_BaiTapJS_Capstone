function Product(id, productName, price, screen, backCamera, frontCamera, img, desc,type){
    this.id = id;
    this.productName = productName;
    this.price = price;
    this.screen = screen;
    this.backCamera = backCamera;
    this.frontCamera = frontCamera;
    this.img = img;
    this.desc = desc;
    this.type = type;
}